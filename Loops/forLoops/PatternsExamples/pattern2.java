public class pattern2 {
    public static void main(String[] args) {
        for(int i = 1;i<=4;i++){ //i can be any number, but logic is same
            for(int j = 1;j<=i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }    
}
